<?php

public class Employee {
    private String name;
    private Date dob;

    public String getName() {
        return name;
    }
    public setName(String name) {
        this.name = name;
    }
    public Date getDob() {
        return dob;
    }
    public setDob(Date dob) {
        this.dob = dob;
    }
}